# STEP 1: Connect Data
# Open Tableau Desktop
# Click on "Connect" → Choose Excel / CSV
# Load your dataset
# Go to Sheet 1

# STEP 2: Create First Visualization
# Drag a Dimension (e.g., Category) to Rows
# Drag a Measure (e.g., Sales) to Columns
# Tableau will create a chart (bar chart by default)
# Use "Show Me" to change chart type if needed

# STEP 3: Create Multiple Sheets
# Click on "New Worksheet" (+ icon)
# Create different charts (bar, line, pie, map, etc.)
# Each sheet represents one visualization

# STEP 4: Create Dashboard
# Click on "New Dashboard"
# Drag sheets from left panel into dashboard area
# Arrange using Tiled or Floating layout

# STEP 5: Add Filters (Slicers)
# Drag a field (e.g., Category) to Filters shelf
# Right-click it → "Show Filter"
# OR click chart dropdown → "Use as Filter"
# Choose filter type: dropdown, checkbox, slider

# STEP 6: Apply Filter to All Charts
# Click filter dropdown arrow
# Select "Apply to Worksheets" → "All Using This Data Source"

# STEP 7: Sorting
# Click sort icon on chart toolbar (ascending/descending)
# OR right-click axis → Sort
# OR sort directly from data pane

# STEP 8: Add Interactivity
# Enable "Use as Filter" on charts
# Add highlight actions if needed
# Add tooltips for extra info

# STEP 9: Format Dashboard
# Add titles, text, colors
# Adjust size and layout
# Use containers for better alignment

# STEP 10: Save / Export
# Save as .twb or .twbx file
# Export as PDF or Image if needed
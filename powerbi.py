# STEP 1: Open Power BI & Load Data
# Open Power BI Desktop
# Click "Get Data" → Select Excel / CSV / SQL
# Click "Load" (or "Transform Data" if cleaning is needed)

# STEP 2: Data Preparation (Optional)
# Click "Transform Data" to open Power Query
# Clean data (remove nulls, rename columns, change data types)
# Click "Close & Apply"

# STEP 3: Create First Visualization
# Go to "Report" view
# Select a chart from Visualizations pane (e.g., bar chart)
# Drag fields:
#   Axis → Category
#   Values → Sales

# STEP 4: Add Multiple Visuals
# Add more charts like:
#   Line chart (for time data)
#   Pie chart
#   Table or Card (for KPIs)
# Arrange them on the canvas

# STEP 5: Add Slicer (Filter)
# Click "Slicer" visual
# Drag a field (e.g., Category or Date) into it
# Change slicer type:
#   Dropdown
#   List
#   Between (for numeric/date ranges)

# STEP 6: Control Filter Interaction
# Click on slicer
# Go to "Format" → "Edit Interactions"
# Choose which visuals it should filter

# STEP 7: Sorting
# Select a visual
# Click three dots (⋯)
# Choose "Sort by" → Select field
# Choose Ascending or Descending

# STEP 8: Add Filters (Advanced)
# Use Filters pane:
#   "Filters on this visual"
#   "Filters on this page"
#   "Filters on all pages"
# Drag fields and set conditions

# STEP 9: Add Interactivity
# Enable cross-filtering between visuals
# Use drill-down for hierarchy (e.g., Year → Month)
# Add tooltips for extra details

# STEP 10: Format Dashboard
# Use Format pane:
#   Titles, labels, colors
#   Background and layout
# Align visuals neatly

# STEP 11: Save & Publish
# Save file as .pbix
# Click "Publish" to upload to Power BI Service
# Share dashboard if needed